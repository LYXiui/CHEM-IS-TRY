/**
 * 標準週期表格線（參考 Wikipedia 18 欄 × 9 列配置）
 * row/col 為 0-based；null 為留白
 */
export const PT_COLS = 18
export const PT_ROWS = 9

const R = (row, entries) => {
  const line = Array(PT_COLS).fill(null)
  entries.forEach(([col, symbol]) => {
    line[col] = symbol
  })
  return { row, cells: line }
}

